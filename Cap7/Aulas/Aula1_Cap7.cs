            /* ------ O que é herança? -------

            Herança é um tipo de associação que permite que uma classe herde dados e comportamentos de outra, visando dessa
            forma deixar mais prática e dinâmica a criação de projetos, sem ter a necessidade de ficar criando diferentes
            classes por completo, repetindo atributos e métodos.

            --- Vantagens ---
            - Reuso
            - Polimorfismo

            ----- Definições -----

            - Relação "é-um" (ou seja, a classe que estará herdando as características de outra, é uma classe da mesma "linha-
            gem").

            - Generalização/especialização (isso se deve ao fato de que, a classe que estará repassando suas informações para
            outras é uma generalização, e as outras se tornam uma especialização da mesma, já que cada uma contará com sua es-
            pecificidade).

            - Superclasse e Subclasses (o conceito se deriva da ideia de que teremos uma classe que performará como base {Super-
            classe} e outras que herdarão suas características para criar suas próprias classes {Subclasses}).

            - Herança e Extensão (o conceito se refere a ideia de que as subclasses herdam as características da superclasse e
            proferem uma extensão a mesma para que se encaixe as suas necessidades).

            - Herança é uma associação entre classes e não entre objetos (isso se deve ao fato de não ser necessário instanciarmos
            dois objetos, ou mais, para criar um sistema completo, em herança ao instanciarmos um objeto, que conterá todos os dados
            necessários dentro das suas necessidades).
             */
        }
    }
